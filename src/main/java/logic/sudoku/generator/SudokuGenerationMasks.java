    private static int shiftCoord(int coord, int shift) {
        if ((coord + shift) > 8) {
            return shift - (8 - coord) - 1;
        }

        return coord + shift;
    }

    private static int[][] shiftMask(int[][] initialMask) {
        int[][] shiftedMask = new int[initialMask.length][2];

        int horizontalShift = RandomNums.randomInt(1, 8);
        int verticalShift = RandomNums.randomInt(1, 8);

        for (int i = 0; i < shiftedMask.length; i++) {
            shiftedMask[i][0] = SudokuGenerationMasks.shiftCoord(initialMask[i][0], verticalShift);
            shiftedMask[i][1] = SudokuGenerationMasks.shiftCoord(initialMask[i][1], horizontalShift);
        }

        return shiftedMask;
    }

    private static int[][] getRandomMask(int[][][] masks) {
       int index = RandomNums.randomInt(0, masks.length - 1);
       return masks[index];
    }

    public static int[][] getMask(SudokuDifficulty difficulty) {
        int[][] randomMask = null;
        switch (difficulty) {
            case EASY:
               randomMask = SudokuGenerationMasks.getRandomMask(easyMasks);
               break;

            case MEDIUM:
                randomMask = SudokuGenerationMasks.getRandomMask(mediumMasks);
                break;

            case HARD:
                randomMask = SudokuGenerationMasks.getRandomMask(hardMasks);
                break;
        }

        return shiftMask(randomMask);
    }
